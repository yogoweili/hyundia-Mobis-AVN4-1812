static int tw8836_i2c_read(unsigned char cmd, unsigned char *recv_data)
{
	int ret = -1;
	int count = I2C_RETRY_COUNT;

	do {
		ret = i2c_xfer(SLAVE_ADDRESS_TW8836, 1, &cmd, 1, recv_data, I2C_CH_TW8836);
		if (ret == 0)
			return ret;
		else
			count--;

	} while(count > 0);

	return ret;
}

static int tw8836_i2c_write(unsigned char cmd, unsigned char value)
{
	unsigned char send_data[2];
	int ret = -1;
	int count = I2C_RETRY_COUNT;
	send_data[0] = cmd;
	send_data[1] = value;

	do {
		ret = i2c_xfer(SLAVE_ADDRESS_TW8836, 2, send_data, 0, 0, I2C_CH_TW8836);
		if (ret == 0)
			return ret;
		else
			count--;

	} while(count > 0);

	return ret;
}

int tw8836_write_value(unsigned char page,
		int address_size, const unsigned char* addresses, char value)
{
	int i;
	int ret = tw8836_i2c_write(MOVE_PAGE_ADDRESS, page);
	if (ret != 0)
		//failed to move page.
		return FAIL_I2C;

	for (i = 0; i < address_size; i++)
	{
		ret = tw8836_i2c_write(addresses[i], value);
		if (ret != 0)
			return FAIL_I2C;
	}

	//Ignore whether the page has been moved 0.
	tw8836_i2c_write(MOVE_PAGE_ADDRESS, 0x00);

	return SUCCESS_;
}

int tw8836_set_ie(int cmd, unsigned char level)
{
	int ret = -1;

	if (level <= TW8836_BRIGHTNESS_LIMITS[0] ||
            level > TW8836_BRIGHTNESS_LIMITS[1])
		return ret;

	switch (cmd)
	{
		case SET_BRIGHTNESS:
			ret = tw8836_write_value(TW8836_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_ADDRESSES_BRIGHTNESS), I2C_ADDRESSES_BRIGHTNESS, level);
			break;

		case SET_CONTRAST:
			ret = tw8836_write_value(TW8836_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_ADDRESSES_CONTRAST), I2C_ADDRESSES_CONTRAST, level);
			break;

		case SET_HUE:
			ret = tw8836_write_value(TW8836_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_ADDRESSES_HUE), I2C_ADDRESSES_HUE, level);
			break;

		case SET_SATURATION:
			ret = tw8836_write_value(TW8836_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_ADDRESSES_SATURATION), I2C_ADDRESSES_SATURATION, level);
			break;

		case SET_CAM_BRIGHTNESS:
			ret = tw8836_write_value(TW8836_CAM_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_CAM_ADDRESSES_BRIGHTNESS), I2C_CAM_ADDRESSES_BRIGHTNESS, level);
			break;

		case SET_CAM_CONTRAST:
			ret = tw8836_write_value(TW8836_CAM_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_CAM_ADDRESSES_CONTRAST), I2C_CAM_ADDRESSES_CONTRAST, level);
			break;

		case SET_CAM_HUE:
			ret = tw8836_write_value(TW8836_CAM_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_CAM_ADDRESSES_HUE), I2C_CAM_ADDRESSES_HUE, level);
			break;

		case SET_CAM_SATURATION:
			ret = tw8836_write_value(TW8836_CAM_IMAGE_ENHANCEMENT_PAGE,
					sizeof(I2C_CAM_ADDRESSES_SATURATION), I2C_CAM_ADDRESSES_SATURATION, level);
			break;
	}

	return -1;
}

