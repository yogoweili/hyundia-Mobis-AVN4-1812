void tw8836_init()
{
	int ret_mxt = has_MXT336S();
	int i, size, ret = 0;
	unsigned char *read_data = 0;

	LKPRINT("%s has_MXT336S: %d\n", __func__, ret_mxt);

	//TW8836 reset
	gpio_set(TW8836_RESET_GPIO, 0);
	gpio_set(TW8836_RESET_GPIO, 1);
	lcd_delay_us(10);

	//select 0 page
	ret = tw8836_i2c_write(0xFF, 0x00);
	if (ret != 0)
	{
		//error
		return;
	}

	//read id
	ret = tw8836_read_id(read_data);
	if (ret == 0)
	{
		LKPRINT("%s TW8836 id: 0x%x \n", __func__, *read_data);
	}
	else
	{
		LKPRINT("%s failed to read id.\n", __func__);
	}

	size = ret_mxt ? ARRAY_SIZE(TW8836_INIT_REGS_FOR_AUO_LCD) : ARRAY_SIZE(TW8836_INIT_REGS);
	for (i = 0; i < size; i++)
	{
		const unsigned char cmd = ret_mxt ?
			TW8836_INIT_REGS_FOR_AUO_LCD[i].cmd : TW8836_INIT_REGS[i].cmd;
		const unsigned char value = ret_mxt ?
			TW8836_INIT_REGS_FOR_AUO_LCD[i].value : TW8836_INIT_REGS[i].value;

		ret = tw8836_i2c_write(cmd, value);
	}

	//test tw8836_get_bt656();
	LKPRINT("%s finished. (i2c count: %d)\n", __func__, size);
}

void tw8836_analog_input_init()
{
	unsigned int i;

	for (i = 0 ; i < ARRAY_SIZE(TW8836_ANALOG_INPUT_INIT_REGS); i++)
	{
		const unsigned char cmd = TW8836_ANALOG_INPUT_INIT_REGS[i].cmd;
		const unsigned char value = TW8836_ANALOG_INPUT_INIT_REGS[i].value;
		LKPRINT("%s cmd:0x%x, value:0x%x\n", __func__, cmd, value);
		tw8836_i2c_write(cmd, value);
	}
}

// Check ATMEL MXT336S & AUO LCD
int has_MXT336S(void)
{
	int ret = 0;
	unsigned int i;

	for (i = 0; i < ARRAY_SIZE(MXT336S_AT_IC_ADDRESS) ; i++)
	{
		unsigned char cmd = 0x00;
		unsigned char recv_data = 0;

		ret = i2c_xfer(MXT336S_AT_IC_ADDRESS[i], 1, &cmd, 1, &recv_data, I2C_CH_MXT336S_AT);
		if (ret >= 0)
		{
			ret = 1;
			break;
		}
		else
			ret = 0;
	}

	return ret;
}

